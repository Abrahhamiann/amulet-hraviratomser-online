type Key = keyof typeof dict.en;

const AdminI18nContext = createContext<{
  lang: AdminLang;
  setLang: (lang: AdminLang) => void;
  t: (key: Key) => string;
} | null>(null);

function getInitialLang(): AdminLang {
  if (typeof window === "undefined") return "hy";
  const value = localStorage.getItem(STORAGE_KEY);
  return value === "en" || value === "ru" || value === "hy" ? value : "hy";
}

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<AdminLang>(getInitialLang);
  const value = useMemo(
    () => ({
      lang,
      setLang(next: AdminLang) {
        localStorage.setItem(STORAGE_KEY, next);
        setLangState(next);
      },
      t(key: Key) {
        return dict[lang][key] || dict.en[key] || key;
      },
    }),
    [lang],
  );

  return <AdminI18nContext.Provider value={value}>{children}</AdminI18nContext.Provider>;
}

export function useAdminI18n() {
  const value = useContext(AdminI18nContext);
  if (!value) throw new Error("useAdminI18n must be used inside AdminI18nProvider");
  return value;
}
